    private void displayEtudiants(List resultList) {
        Vector<String> tableHeaders = new Vector<String>();
        Vector tableData = new Vector();
        tableHeaders.add("nom");
        tableHeaders.add("prenom");
        tableHeaders.add("adressePostal");
        tableHeaders.add("groupe");
        
        for(Object o : resultList) {
            Etudiant etudiant = (Etudiant)o;
            Vector<Object> oneRow = new Vector<Object>();
            oneRow.add(etudiant.getNom());
            oneRow.add(etudiant.getPrenom());
            oneRow.add(etudiant.getAdressepostal());
            oneRow.add(etudiant.getGroupe());
        }
        jTable1.setModel(new DefaultTableModel(tableData, tableHeaders));
    }
